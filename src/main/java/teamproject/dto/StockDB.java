package teamproject.dto;

public class StockDB {
	private String stockColor;
	private int stockSizeS;
	private int stockSizeM;
	private int stockSizeL;
	private int stockSizeXL;
	private long productCode;
	private String stockName;
	
	public String getStockColor() {
		return stockColor;
	}
	public void setStockColor(String stockColor) {
		this.stockColor = stockColor;
	}
	public int getStockSizeS() {
		return stockSizeS;
	}
	public void setStockSizeS(int stockSizeS) {
		this.stockSizeS = stockSizeS;
	}
	public int getStockSizeM() {
		return stockSizeM;
	}
	public void setStockSizeM(int stockSizeM) {
		this.stockSizeM = stockSizeM;
	}
	public int getStockSizeL() {
		return stockSizeL;
	}
	public void setStockSizeL(int stockSizeL) {
		this.stockSizeL = stockSizeL;
	}
	public int getStockSizeXL() {
		return stockSizeXL;
	}
	public void setStockSizeXL(int stockSizeXL) {
		this.stockSizeXL = stockSizeXL;
	}
	public long getProductCode() {
		return productCode;
	}
	public void setProductCode(long productCode) {
		this.productCode = productCode;
	}
	public String getStockName() {
		return stockName;
	}
	public void setStockName(String stockName) {
		this.stockName = stockName;
	}
	
	
}
